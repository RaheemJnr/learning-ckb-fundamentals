/**
 * Walks through each of the 4 bugs in the buggy-lock script, explaining
 * how they manifest, how to find them, and how to fix them.
 */
function printBugWalkthrough(): void {
  console.log(`
  =====================================================================
  Bug Walkthrough: The 4 Bugs in buggy-lock
  =====================================================================

  The buggy-lock script (contracts/buggy-lock/src/main.rs) implements a
  blake2b hash-based lock with 4 intentional bugs. Here is how to find
  and fix each one.

  ----- Bug #1: Off-by-one in args reading -----

  Location: Step 1, where expected_hash is extracted from args
  Buggy code:
    let expected_hash = &args[1..BLAKE2B_HASH_LEN + 1];
                             ^-- starts at 1 instead of 0

  Symptom:
    - The script reads bytes 1..33 instead of bytes 0..32
    - If args is exactly 32 bytes, this causes a runtime panic
      (index out of bounds), which shows up as a VM error
    - If args is longer, the script silently reads the wrong hash,
      causing ERROR_HASH_MISMATCH even with the correct preimage

  How to find it:
    - Add ckb_debug! to print the first few bytes of expected_hash
    - Compare with the hash you put in the args
    - The first byte will differ (shifted by one position)

  Fix:
    let expected_hash = &args[0..BLAKE2B_HASH_LEN];

  ----- Bug #2: Wrong witness index -----

  Location: Step 2, where the witness is loaded
  Buggy code:
    let witness = match load_witness(1, Source::Input) {
                                     ^-- index 1 instead of 0

  Symptom:
    - If the transaction has only 1 witness, this fails with
      INDEX_OUT_OF_BOUND (-1 VM error)
    - If there are multiple witnesses, it loads the WRONG witness,
      causing hash mismatch

  How to find it:
    - The error code will be -1 (INDEX_OUT_OF_BOUND) for single-witness txs
    - Add ckb_debug! before and after the load_witness call
    - The "after" message will never print if it fails

  Fix:
    let witness = match load_witness(0, Source::Input) {

  ----- Bug #3: Incorrect hash comparison length -----

  Location: Step 4, where computed_hash is compared to expected_hash
  Buggy code:
    let compare_len = 20; // Should be BLAKE2B_HASH_LEN (32)

  Symptom:
    - Only 20 of 32 bytes are compared
    - The script appears to work correctly in most tests because
      the first 20 bytes will differ for different preimages
    - This is a SECURITY vulnerability, not a functional bug in most cases
    - It reduces hash security from 256 bits to 160 bits

  How to find it:
    - This bug is subtle and may not show up in basic testing
    - Code review is the best way to catch it
    - A fuzzer that generates preimages with the same first 20 bytes
      but different remaining bytes would expose it

  Fix:
    let compare_len = BLAKE2B_HASH_LEN; // 32

  ----- Bug #4: Missing error code return -----

  Location: End of the function, after hash comparison fails
  Buggy code:
    debug!("buggy-lock: hash comparison FAILED");
    0 // This incorrectly returns success even when the hash doesn't match!

  Symptom:
    - The script ALWAYS returns 0 (success), even with the wrong preimage
    - This makes the lock script completely useless — anyone can spend
    - In testing, if you only test with the correct preimage, you will
      never notice this bug

  How to find it:
    - Test with an INCORRECT preimage — the script should fail but does not
    - Add ckb_debug! and notice that the FAILED message prints but the
      script still returns 0
    - This is why negative testing (testing failure cases) is critical

  Fix:
    ERROR_HASH_MISMATCH  // Return error code 3 instead of 0
  `);
}

