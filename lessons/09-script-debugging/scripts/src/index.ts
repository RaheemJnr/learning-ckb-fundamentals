// Section 5: Using ckb_debug! for Print Debugging
// ============================================================================

/**
 * The ckb_debug! macro is the primary way to add "print" debugging to
 * CKB scripts. Since scripts run in an isolated VM with no stdout,
 * ckb_debug! uses a special debug syscall that outputs messages when
 * the script runs under CKB-Debugger.
 *
 * In production (on the real CKB node), ckb_debug! calls are ignored
 * — they consume a small number of cycles but produce no visible output.
 */
function printDebugMacroGuide(): void {
  console.log(`
  =====================================================================
  Using ckb_debug! for Print Debugging
  =====================================================================

  The ckb_debug! macro is provided by the ckb-std crate and works like
  Rust's println! macro but outputs to the CKB-Debugger's stderr.

  Setup in Cargo.toml:
    [dependencies]
    ckb-std = "0.16"

  Usage in your script:
    use ckb_std::debug;

    pub fn program_entry() -> i8 {
        // Basic message
        debug!("script execution started");

        // With variables (supports format strings like println!)
        let args_len = args.len();
        debug!("args length: {}", args_len);

        // With hex data
        debug!("first 4 bytes of args: {:02x}{:02x}{:02x}{:02x}",
            args[0], args[1], args[2], args[3]);

        // Trace control flow
        if some_condition {
            debug!("taking the success path");
            return 0;
        }

        debug!("taking the error path");
        return 1;
    }

  Running with debug output:
    ckb-debugger \\
      --tx-file tx.json \\
      --script-group-type lock \\
      --cell-index 0 \\
      --bin ./build/debug/my-script

    Output:
      DEBUG: script execution started
      DEBUG: args length: 32
      DEBUG: first 4 bytes of args: a1b2c3d4
      DEBUG: taking the error path
      Run result: 1

  Tips for effective debug printing:
    1. Print at ENTRY and EXIT points of your script
    2. Print the LENGTH of all loaded data before using it
    3. Print the first few bytes of hashes for comparison
    4. Print which BRANCH the code takes at every if/match
    5. Print loop counters and iteration values
    6. Use a PREFIX in all messages (e.g., "my-lock: ...") so you can
       tell which script produced which output when multiple scripts run

  Performance note:
    - ckb_debug! calls consume a small number of VM cycles (~2000 per call)
    - In production, you may want to remove them or gate behind a feature flag
    - For development and testing, the overhead is negligible

  Conditional compilation (optional):
    #[cfg(debug_assertions)]
    debug!("this only compiles in debug mode");

    // Or use a feature flag:
    #[cfg(feature = "debug-output")]
    debug!("this only compiles when the feature is enabled");
  `);
}

