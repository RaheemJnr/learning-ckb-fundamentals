// Section 4: Reading Error Output from Failed Transactions
// ============================================================================

/**
 * When a transaction fails on the CKB node, the RPC returns an error
 * response with structured information about what went wrong.
 *
 * Here is what a typical error response looks like and how to interpret it.
 */
function printErrorOutputGuide(): void {
  console.log(`
  =====================================================================
  Reading Error Output from Failed Transactions
  =====================================================================

  When you submit a transaction to a CKB node and it fails script
  verification, you will receive an error response like this:

  Example RPC Error Response:
  {
    "code": -302,
    "message": "TransactionFailedToVerify: Verification failed Script(
      TransactionScriptError {
        source: Inputs[0].Lock,
        cause: script exit code: 3
      }
    )",
    "data": "Verification(Error { ... })"
  }

  Breaking this down:

  1. "code": -302
     This is the CKB RPC error code for "TransactionFailedToVerify".
     It means the transaction was well-formed but a script rejected it.

  2. "source": Inputs[0].Lock
     This tells you WHICH script failed:
       - Inputs[0]  = The first input cell (index 0)
       - .Lock      = The lock script of that cell
     Other possibilities:
       - Inputs[N].Type  = Type script of input N
       - Outputs[N].Type = Type script of output N

  3. "cause": script exit code: 3
     This is the exit code returned by your script.
     - Positive numbers (1, 2, 3, ...) = Your script returned this code
     - Negative numbers (-1, -2, ...) = CKB-VM returned this code
     - 0 would be success (but then you would not see this error!)

  Using the CCC SDK, you can catch and parse these errors:

    try {
      const txHash = await client.sendTransaction(tx);
    } catch (error) {
      // The error message contains the script source and exit code.
      // Parse it to identify which script failed and why.
      console.error("Transaction failed:", error.message);

      // Look for patterns like "Inputs[N].Lock" or "Inputs[N].Type"
      // and "exit code: N" to extract the relevant information.
    }

  Common RPC error codes:
    -301  TransactionFailedToResolve  (missing cell deps, inputs not found)
    -302  TransactionFailedToVerify   (a script returned non-zero exit code)
    -303  PoolRejectedDuplicatedTx    (transaction already in the pool)
    -304  PoolIsFull                  (transaction pool is at capacity)
    -311  PoolRejectedMalformedTx     (transaction structure is invalid)
  `);
}

