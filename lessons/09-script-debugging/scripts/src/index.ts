// Section 2: Debugging Methodology
// ============================================================================

/**
 * A systematic approach to debugging CKB scripts:
 *
 * Step 1: READ THE ERROR
 *   - The CKB node or SDK will report which script failed and its exit code.
 *   - Identify whether it is a VM error (negative) or script error (positive).
 *   - Check the error against known codes (see the tables above).
 *
 * Step 2: REPRODUCE LOCALLY
 *   - Dump the failing transaction to a JSON file.
 *   - Use CKB-Debugger to run the script offline with the same inputs.
 *   - This gives you full control and faster iteration.
 *
 * Step 3: ADD DEBUG PRINTS
 *   - Insert ckb_debug!() calls at key points in your Rust script.
 *   - Recompile and run under CKB-Debugger to see the debug output.
 *   - Trace the execution path to find where it diverges from expectations.
 *
 * Step 4: ISOLATE THE BUG
 *   - Once you know the failing code path, examine each operation.
 *   - Common pitfalls: wrong indices, incorrect lengths, missing error returns.
 *   - Check your assumptions about data layout and encoding.
 *
 * Step 5: FIX AND VERIFY
 *   - Apply the fix and run under CKB-Debugger again.
 *   - Test both the happy path (should succeed) and error paths (should fail).
 *   - Deploy the fixed script and verify on testnet.
 */

