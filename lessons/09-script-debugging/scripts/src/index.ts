/**
 * CKB has two layers of error codes:
 *
 * 1. VM-Level Errors (negative numbers):
 *    These come from the CKB-VM itself and indicate problems with the script
 *    binary or execution environment, not with your script's logic.
 *
 * 2. Script-Level Errors (positive numbers):
 *    These are returned by your script code via the exit code (i8 return value).
 *    You define what each code means in your script.
 *
 * When a transaction fails validation, the CKB node reports which script failed
 * and what exit code it returned. Understanding these codes is the first step
 * in debugging.
 */

